public class Imoveis {
	private String codigo;
	private double area;
	private String tipoImoveis;
	private String bairro;
	private int numeroQuartos;
	private int numroDeBanheiros;
	private int garagens;
	private double precoImoveis;

	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	public String getTipoImoveis() {
		return tipoImoveis;
	}
	public void setTipoImoveis(String tipoImoveis) {
		this.tipoImoveis = tipoImoveis;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public int getNumeroQuartos() {
		return numeroQuartos;
	}
	public void setNumeroQuartos(int numeroQuartos) {
		this.numeroQuartos = numeroQuartos;
	}
	public int getNumroDeBanheiros() {
		return numroDeBanheiros;
	}
	public void setNumroDeBanheiros(int numroDeBanheiros) {
		this.numroDeBanheiros = numroDeBanheiros;
	}
	public int getGaragens() {
		return garagens;
	}
	public void setGaragens(int garagens) {
		this.garagens = garagens;
	}
	public double getPrecoImoveis() {
		return precoImoveis;
	}
	public void setPrecoImoveis(double precoImoveis) {
		this.precoImoveis = precoImoveis;
	}


}
